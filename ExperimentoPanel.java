    private void estRealizarExpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estRealizarExpActionPerformed
        
        // EXPERIMENTO ESTANDARD
        int q, m, eI, eF;
        double e;
        char sexo;
        String patologia;
        
        if (estError.getText().equals("") || estLongitud.getText().equals("") || estMinimAp.getText().equals("")){
            JOptionPane.showMessageDialog(null, "ATENCION: los campos 'Longitud', 'Error' y 'Apariciones Minimas' deben estar llenos.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if( estLongitud.getText().length() > 4 || estError.getText().length() > 3 || estMinimAp.getText().length() > 3 ){
            JOptionPane.showMessageDialog(null, "ATENCION: los campos 'Longitud', 'Error' o 'Apariciones Minimas' contienen valores fuera de rango.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if( Integer.parseInt(estError.getText()) > 100){
            JOptionPane.showMessageDialog(null, "ATENCION: el error puede ser entre 0 y 100.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if (Integer.parseInt(estError.getText()) > 100 && estRadioButton.isCursorSet()){
            JOptionPane.showMessageDialog(null, "ATENCION: con el minimo de apariciones en % activado, el valor de 'Apariciones Minimas' debe ser entre 0 y 100.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            m = Integer.parseInt( estLongitud.getText());
            e = Double.parseDouble( estError.getText() )/100;
            if ( estRadioButton.isSelected()) q = (listaSequencias.size() * Integer.parseInt(estMinimAp.getText())) / 100 ;
            else q = Integer.parseInt(estMinimAp.getText());
            ArrayList<String> aux = CtrlPresAlgoritmo.getNombrePatologias();
            patologia = aux.get(estPatBox.getSelectedIndex());
            
            if( selecDir ){
                
                sexo = 'x';
                eI = 0;
                eF = 120;
            } else {
                char[] sexoA = {'x', 'm', 'f'};
                eI      = Integer.parseInt(estEdadMin.getText());
                eF      = Integer.parseInt(estEdadMax.getText());
                sexo   = sexoA[estSexoBox.getSelectedIndex()];
            }
            
            CtrlPresAlgoritmo.experimentoEstandard( listaSequencias, m, e, q, eI, eF, sexo, patologia );
            // Habilita el boto de guardar i inhabilita el de realitzar experiment
            estGuardarResultado.setEnabled(true);
            estRealizarExp.setEnabled(false);
        }
    }//GEN-LAST:event_estRealizarExpActionPerformed
    
    
    private void estButtonCriteriosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estButtonCriteriosActionPerformed
        // Comprovacions
        if (estEdadMin.getText().equals("") || estEdadMax.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "ATENCION: faltan campos por rellenar.\nEs necesario rellenar todos los campos.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if (( estEdadMin.getText().length() > 3) || ( estEdadMax.getText().length() > 3 )) {
            JOptionPane.showMessageDialog(null, "ATENCION: la edad debe ser inferior a 1000.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if ( Integer.parseInt(estEdadMin.getText()) > Integer.parseInt(estEdadMax.getText())) {
            JOptionPane.showMessageDialog(null, "ATENCION: se ha introducido una edad minima superior a la edad maxima.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if (estPatBox.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "ATENCION: aun no se han introducido patologias en el sistema.", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            char[] sexo = {'x', 'm', 'f'};
            ArrayList<String> aux = CtrlPresAlgoritmo.getNombrePatologias();
            listaSequencias = CtrlPaciente.seleccionaPac(sexo[estSexoBox.getSelectedIndex()], Integer.parseInt(estEdadMin.getText()), Integer.parseInt(estEdadMax.getText()), aux.get(estPatBox.getSelectedIndex()));
            if( listaSequencias.size() > 0){
                String text = "";
                for (int i = 0; i <listaSequencias.size(); i++) text += i+1 + ". " + listaSequencias.get(i) + "\n";
                text += "\nPresione sobre 'Realizar Experimento' para llevar a cabo el mismo.";
                estAreaEntrada.setText(text);
                estRealizarExp.setEnabled(true);
                selecDir = false;
            } else {
                JOptionPane.showMessageDialog(null, "ATENCION: los pacientes seleccionados no tienen ristras de ADN.", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_estButtonCriteriosActionPerformed

