    void actualizaPatologias() {
        ArrayList<String> aux = CtrlPresAlgoritmo.getNombrePatologias();
        estPatBox.removeAllItems();
        genPatBox.removeAllItems();
        for(int i = 0; i < aux.size(); i++){
            estPatBox.addItem(aux.get(i));
            genPatBox.addItem(aux.get(i));
        }
    }

    void actualizaPacientes() {
        ArrayList<Integer> aux = CtrlPresAlgoritmo.getNombrePacientes();
        potModelPac.clear();
        for(int i = 0; i < aux.size(); i++){
            potModelPac.addElement(aux.get(i));
        }
        potListaPacientes.setModel(potModelPac);
        patronList.setModel(potModelPac);
    }

    private boolean noContiene(DefaultListModel model, Object obj) {
        for (int i = 0; i < model.getSize(); i++) if(model.getElementAt(i).equals(obj)) return false;
        return true;
    }
